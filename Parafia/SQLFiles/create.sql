CREATE VIEW importantdates AS (
 SELECT
  laybrothers.id,
  dateofbirth,
  MAX(CASE WHEN sacramenttype = 1 THEN massdate ELSE NULL END) AS "s1",
  MAX(CASE WHEN sacramenttype = 2 THEN massdate ELSE NULL END) AS "s2",
  MAX(CASE WHEN sacramenttype = 3 THEN massdate ELSE NULL END) AS "s3",
  deathdate
 FROM
  laybrothers LEFT JOIN initializationsacraments ON (laybrothers.id = initializationsacraments.laybrotherid)
  LEFT JOIN masses ON (initializationsacraments.massid = masses.massid)
  LEFT JOIN deaths ON (deaths.laybrotherid = laybrothers.id)
 GROUP BY 1, 6
 ORDER BY 1
);


--returns null if all is right, otherwise ID
CREATE OR REPLACE FUNCTION checksacramentsintegrity() RETURNS INT AS
$checksacramentsintegrity$
DECLARE
 row RECORD;
 date1 DATE;
 date2 DATE;
 date3 DATE;
 date4 DATE;
 date5 DATE;
BEGIN 
 FOR row IN (SELECT * FROM importantdates) --check if the dates are ordered properly
 LOOP
  date1 = row.dateofbirth;
  IF (row.s1 IS NULL)
  THEN
   date2 = date1;
  ELSE
   date2 = row.s1;
  END IF;

  IF (row.s2 IS NULL)
  THEN
   date3 = date2;
  ELSE
   date3 = row.s2;
   IF (date3 < (date1+INTERVAL'3 years')::date)
   THEN
    RETURN row.id;
   END IF;
  END IF;

  IF (row.s3 IS NULL)
  THEN
   date4 = date3;
  ELSE
   date4 = row.s3;
  END IF;

  IF (row.deathdate IS NULL)
  THEN
   date5 = date4;
  ELSE
   date5 = row.deathdate;
  END IF;

  IF (NOT(date1 <= date2 AND date2 <= date3 AND date3 <= date4 AND date4 <= date5))
  THEN
   RETURN row.id;
  END IF;
 END LOOP;
 RETURN NULL;
END;
$checksacramentsintegrity$
language plpgsql;

